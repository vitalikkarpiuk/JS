function pow(x, y) {
    // Перевіряємо, чи y є цілим числом
    if (!Number.isInteger(y)) {
        return NaN; // Якщо y не ціле число, повертаємо NaN
    }
    
    // Якщо степінь y дорівнює 0, повертаємо 1 (для будь-якого числа, крім 0)
    if (y === 0) {
        return 1;
    }

    // Якщо степінь y менше 0, міняємо х на обернене число, а y на від'ємний
    if (y < 0) {
        x = 1 / x;
        y = -y;
    }

    // Ініціалізуємо змінну результату
    let result = 1;
    // Підносимо число x до степеня y
    for (let i = 0; i < y; i++) {
        result *= x;
    }
    // Повертаємо результат
    return result;
}

// Приклади використання функції
console.log(pow(2, 3)); // Виведе результат 8
console.log(pow(4, 3)); // Виведе результат 64
console.log(pow(2, -1)); // Виведе результат 0.5
console.log(pow(3, 1.5)); // Виведе результат NaN (оскільки степінь не є цілим числом)
