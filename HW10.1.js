// В цьому регулярньму виразі перевіряємо присутність Аа (англ) Аа (кирилиця)
const regex = /^(?!.*[AaАа]).{1,255}$/;

// Вводимо тест для пошуку літер Аа:
const testString = "Your text";

// Перевіряємо чи відповідає текст критеріям регулярного виразу:
if (regex.test(testString)) {
    console.log("Даний рядок відповідає критеріям.");
} else {
    console.log("Даний рядок не відповідає критеріям.");
}